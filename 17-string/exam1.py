# 대/소 문자 변환
st1 = "He likes apples, she likes too"

# 문자열을 대문자로
st2 = st1.upper()
print(st1)
print(st2)
print('-' * 30)

# 문자열을 소문자로
st2 = st1.lower()
print(st1)
print(st2)
print('-' * 30)

# 문자열을 대문자는 소문자로, 소문자는 대문자로
st2 = st1.swapcase()
print(st1)
print(st2)
print('-' * 30)

# 각단어의 첫글자를 대문자로
st3 = st2.title()
print(st2)
print(st3)
print('-' * 30)

# 문자열의 첫글자를 대문자로
st4 = st3.capitalize()
print(st3)
print(st4)
print('-' * 30)






