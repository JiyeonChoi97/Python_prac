# 문자열 출력
print("Hello Python")

# 계산식
print(5 + 7)

# 문자열과 숫자 출력
print(5, "+", 7, "=", 5+7)

# 문자열과 숫자의 + 결합은 안된다. 
#print("나이 : " + 25) ---> (X)
print("나이 : ", 25)

# 서식문자
# => %s : 문자열
# => %d : 정수
# => %f : 실수
print("%s : %d" %('나이', 25))

# 문자열 + 결합
print("강아지" + "고양이")

# 문자열과 숫자의 * 결합
print("안녕" * 5)

# sep 속성 사용
print('Hello', "python", 'Fighting!!', sep='#')

# end 속성 사용
print('Hello', end="ttt")
print("안녕" * 5)






